
public class Print {

	/**
	 * Print the array
	 * @param arr
	 * @throws Exception
	 */
	public void print(String[] arr)throws Exception{
	  for(String x:arr){
		  System.out.println(x);
		  
	  }
	}
	/**
	 * Print double
	 * @param double
	 * @throws Exception
	 */
	public void print(double number){
		System.out.println(number);
	}	
	
}
